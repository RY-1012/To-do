const mongoose = require('mongoose');

const taskSchema = new mongoose.Schema({
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  text: {
    type: String,
    required: true
  },
  emoji: {
    type: String,
    default: 'üìù'
  },
  status: {
    type: String,
    enum: ['not-started', 'in-progress', 'done'],
    default: 'not-started'
  },
  group: {
    type: String,
    default: null
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model('Task', taskSchema);

